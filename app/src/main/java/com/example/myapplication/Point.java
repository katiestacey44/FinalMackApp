package com.example.myapplication;

public class Point {
    private int x; //x coordinate
    private int y; //y coordinate

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
}
